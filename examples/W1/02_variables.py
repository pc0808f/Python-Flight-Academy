"""
W1 範例 2：變數與資料型態
學習重點：int, float, str, bool
"""

# 整數 (Integer)
age = 16
student_count = 30
year = 2026

print("年齡：", age)
print("學生人數：", student_count)
print("年份：", year)
print("age 的資料型態：", type(age))
print()

# 浮點數 (Float)
height = 1.75
weight = 62.5
pi = 3.14159

print("身高：", height, "公尺")
print("體重：", weight, "公斤")
print("圓周率：", pi)
print("height 的資料型態：", type(height))
print()

# 字串 (String)
name = "王小明"
school = "內湖高工"
message = '學習 Python 很有趣！'

print("姓名：", name)
print("學校：", school)
print("訊息：", message)
print("name 的資料型態：", type(name))
print()

# 布林值 (Boolean)
is_student = True
has_license = False

print("是學生：", is_student)
print("有駕照：", has_license)
print("is_student 的資料型態：", type(is_student))
